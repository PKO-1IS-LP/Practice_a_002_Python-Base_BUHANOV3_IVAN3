import tkinter as tk
# Импортируем библиотеку tkinter для создания графического интерфейса
# as tk - создаем сокращение, чтобы писать tk.Button вместо tkinter.Button

window = tk.Tk()
# Создаем главное окно приложения
# Tk() - это конструктор класса, создающий базовое окно

window.title("Три блока")
# Устанавливаем заголовок окна
# title() - метод для изменения текста в заголовке окна

window.geometry("600x300")
# Задаем размер окна: ширина 600, высота 300 пикселей
# Формат: "ширина x высота"

window.configure(bg="white")
# Настраиваем фон окна на белый цвет
# bg (background) - параметр цвета фона

# СОЗДАНИЕ ПЕРВОГО БЛОКА
frame1 = tk.Frame(
    # Frame - это контейнер (прямоугольная область) для других виджетов
    window,
    # Первый аргумент - родительский элемент (главное окно)
    
    width=180,
    # Задаем ширину фрейма 180 пикселей
    
    height=260,
    # Задаем высоту фрейма 260 пикселей
    
    bg="#FF4040"
    # Устанавливаем красный цвет фона для этого фрейма
    # "#FF4040" - ярко-красный цвет в HEX-формате
)

frame1.pack(side="left", padx=5, pady=20)
# Размещаем первый фрейм в окне
# pack() - метод компоновки
# side="left" - размещаем слева (горизонтальное расположение)
# padx=5 - внешний отступ по горизонтали (5 пикселей между фреймами)
# pady=20 - внешний отступ по вертикали (20 пикселей от верха и низа)

frame1.pack_propagate(False)
# Запрещаем фрейму менять свой размер под содержимое
# По умолчанию Frame сжимается под размер вложенных виджетов
# False означает: "не подстраивайся, сохраняй заданные width и height"

label1 = tk.Label(
    # Создаем текстовую метку для первого фрейма
    frame1,
    # Родитель - первый фрейм (метка будет внутри него)
    
    text="Красный",
    # Текст, который будет отображаться на метке
    
    font=("Arial", 28, "bold"),
    # Настройка шрифта: название "Arial", размер 28, жирный ("bold")
    
    fg="white",
    # fg (foreground) - цвет текста (белый)
    
    bg="#FF4040"
    # bg (background) - цвет фона метки (такой же красный, как у фрейма)
    # Это нужно, чтобы метка не выделялась на фоне фрейма
)

label1.pack(expand=True)
# Размещаем метку внутри фрейма
# expand=True - метка будет занимать все доступное пространство
# Благодаря этому текст центрируется по вертикали и горизонтали

# СОЗДАНИЕ ВТОРОГО БЛОКА (аналогично первому, но с другими цветами)
frame2 = tk.Frame(
    window,  # Родитель - главное окно
    width=180,
    height=260,
    bg="#4040FF"  # Синий фон
)
frame2.pack(side="left", padx=5, pady=20)
frame2.pack_propagate(False)

label2 = tk.Label(
    frame2,  # Родитель - второй фрейм
    text="Синий",
    font=("Arial", 28, "bold"),
    fg="white",
    bg="#4040FF"  # Синий фон для метки
)
label2.pack(expand=True)

# СОЗДАНИЕ ТРЕТЬЕГО БЛОКА
frame3 = tk.Frame(
    window,
    width=180,
    height=260,
    bg="#40FF40"  # Зеленый фон
)
frame3.pack(side="left", padx=5, pady=20)
frame3.pack_propagate(False)

label3 = tk.Label(
    frame3,
    text="Зелёный",
    font=("Arial", 28, "bold"),
    fg="white",
    bg="#40FF40"  # Зеленый фон для метки
)
label3.pack(expand=True)

window.mainloop()
# Запускаем главный цикл обработки событий
# Программа будет работать, пока пользователь не закроет окно
# mainloop() постоянно "слушает" события (клики, нажатия клавиш и т.д.)